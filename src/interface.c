/* is_auto_generated_comment()
 *
 * Check if comment line is auto-generated by xnec2c
 */
  static gboolean
is_auto_generated_comment( const char *line )
{
  const char *p;
  char lower[256];
  size_t i, len;

  if( line == NULL )
    return( FALSE );

  len = strlen( line );
  if( len >= sizeof(lower) )
    len = sizeof(lower) - 1;

  for( i = 0; i < len; i++ )
    lower[i] = (char)tolower( (int)line[i] );
  lower[len] = '\0';

  if( strstr(lower, "xnec2c") == NULL )
    return( FALSE );

  p = strstr( lower, "created" );
  if( p == NULL )
    p = strstr( lower, "edited" );

  return( p != NULL );
}

